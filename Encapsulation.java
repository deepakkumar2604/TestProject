package DemoPackage;

public class Encapsulation {


		private int age ;
		
		// Encapsulation --  
		// 1. use private variables
		//2. use getter and setter methods to get and set the values
		
		
	
public void setage(int age) {
	this.age = age;
	
}

public int getage() {
	
	return age ;
}


}