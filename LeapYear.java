
public class LeapYear {

	public static void main(String[] args) {


		leap(2000);
		leap(2001);
		
		
	}
	
	
	
	public static void leap(int year) {
		
		
		
		
		
		if(year %4 ==0 ) {
			System.out.println(" It is leap year");
		}
		else
			System.out.println("It is not an leap year");
		
		
		
		
		
	}

}
